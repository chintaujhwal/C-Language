void main()
{
double a,s,sh,c,ch,t,th;

printf("Enter a Number  :");
scanf("%lf",&a);

s=sin(a);
sh=sinh(a);
c=cos(a);
ch=cosh(a);
t=tan(a);
th=tanh(a);

printf("The TRIGNOMETRIC VALUES are :\n");
printf("Sin     : %lf\n",s);
printf("Sinh    : %lf\n",sh);
printf("Cos     : %lf\n",c);
printf("Cosh    : %lf\n",ch);
printf("Tan     : %lf\n",t);
printf("Tanh    : %lf\n",th);

}
